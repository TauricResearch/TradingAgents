CURRENT_PRICE_FALLBACK_PATTERNS = [
    r"\bat\s+~\$([0-9][0-9,]*(?:\.\d+)?)\b",
    r"near\s+current\s+~?\$([0-9][0-9,]*(?:\.\d+)?)\b",
    r"current\s+~?\$([0-9][0-9,]*(?:\.\d+)?)\s+(?:strength|level|levels|print)",
]


def extract_current_price(report_text: str, decision_text: str, trader_text: str) -> float | None:
    # Current means the report-time latest close, not a live quote.
    market_text = report_text.split("### Sentiment Analyst", 1)[0]
    for pattern in CURRENT_PRICE_PATTERNS:
        m = re.search(pattern, market_text[:35000], flags=re.I)
        if m:
            return parse_money(m.group(1))
    decision_and_trader = f"{decision_text}\n{trader_text}"
    for pattern in CURRENT_PRICE_FALLBACK_PATTERNS:
        m = re.search(pattern, decision_and_trader, flags=re.I)
        if m:
            return parse_money(m.group(1))
    return parse_money(field(trader_text, "Entry Price"))


def extract_confidence(report_text: str) -> str:
    for pattern in (
        r"^\*\*Confidence:\*\*\s*([A-Za-z]+)",
        r"Confidence is \*\*([A-Za-z]+)\*\*",
        r"Confidence:\s*([A-Za-z]+)",
    ):
        m = re.search(pattern, report_text, flags=re.I | re.M)
        if m:
            return m.group(1).capitalize()
    return "n/a"


def horizon_months(horizon: str) -> float | None:
    h = horizon.lower()
    if "4 quarters" in h:
        return 12.0
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*days?", h)
    if m:
        return ((float(m.group(1)) + float(m.group(2))) / 2.0) / 30.4375
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*weeks?", h)
    if m:
        return ((float(m.group(1)) + float(m.group(2))) / 2.0) * 12.0 / 52.0
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*months?", h)
    if m:
        return (float(m.group(1)) + float(m.group(2))) / 2.0
    m = re.search(r"(\d+)\s*months?", h)
    if m:
        return float(m.group(1))
    return None


def build_summary_row(run: Run) -> SummaryRow:
    run_dir = DOCS_DIR / run.ticker / run.folder_name
    decision_text = read_text(run_dir / "5_portfolio" / "decision.md")
    trader_text = read_text(run_dir / "3_trading" / "trader.md")
    report_text = read_text(run_dir / "complete_report.md")

    current = extract_current_price(report_text, decision_text, trader_text)
    target = parse_money(field(decision_text, "Price Target"))
    target_uplift = None
    annualized_uplift = None
    if current and target:
        target_uplift = target / current - 1.0
        months = horizon_months(field(decision_text, "Time Horizon"))
        if months and months > 0 and 1.0 + target_uplift > 0:
            annualized_uplift = math.pow(1.0 + target_uplift, 12.0 / months) - 1.0

    return SummaryRow(
        ticker=run.ticker,
        report_link=f"./{run.ticker}/{run.folder_name}/complete_report.md",
        rating=field(decision_text, "Rating") or "n/a",
        action=field(trader_text, "Action") or "n/a",
        current_price=current,
        price_target=target,
        target_uplift=target_uplift,
        annualized_uplift=annualized_uplift,
        confidence=extract_confidence(report_text),
        horizon=field(decision_text, "Time Horizon") or "n/a",
    )


def latest_runs(
    by_ticker: dict[str, list[Run]], analysis_date: str | None = None
) -> list[Run]:
    analysis_date = normalize_analysis_date(analysis_date)
    runs = []
    for ticker_runs in by_ticker.values():
        if analysis_date:
            ticker_runs = [r for r in ticker_runs if r.analysis_date == analysis_date]
        if not ticker_runs:
            continue
        runs.append(
            sorted(
                ticker_runs,
                key=lambda r: (r.analysis_date, r.run_started),
                reverse=True,
            )[0]
        )
    return runs


def summary_sort_key(row: SummaryRow) -> tuple[int, float]:
    action_rank = {"Buy": 0, "Hold": 1, "Sell": 2}
    return (
        action_rank.get(row.action, 9),
        -(row.target_uplift if row.target_uplift is not None else -999.0),
    )


def format_price(value: float | None) -> str:
    if value is None:
        return "n/a"
    if value >= 1000:
        return f"${value:,.0f}"
    if value >= 100:
        return f"${value:,.2f}"
    return f"${value:.2f}"


def format_percent(value: float | None) -> str:
    if value is None:
        return "n/a"
    return f"{value * 100:+.1f}%"


def short_horizon(horizon: str) -> str:
    return (
        horizon.replace(" months", "m")
        .replace(" month", "m")
        .replace(" weeks", "w")
        .replace(" week", "w")
        .replace(" days", "d")
        .replace(" day", "d")
    )


def build_decision_summary(
    rows: list[SummaryRow], analysis_date: str | None = None
) -> list[str]:
    analysis_date = normalize_analysis_date(analysis_date)
    heading = (
        f"## {analysis_date} Decision Summary"
        if analysis_date
        else "## Latest Decision Summary"
    )
    note = (
        f"_Uses the latest {analysis_date} run folder for each ticker. Current price is the report-time latest close parsed from the report, not a live quote. Target uplift is target/current. 1Y uplift is annualized from the midpoint of the stated horizon, so short-horizon rows can look extreme._"
        if analysis_date
        else "_Uses the latest run folder for each ticker. Current price is the report-time latest close parsed from the report, not a live quote. Target uplift is target/current. 1Y uplift is annualized from the midpoint of the stated horizon, so short-horizon rows can look extreme._"
    )
    lines = [
        heading,
        "",
        note,
        "",
        "| Ticker | Suggestion | Current | Target | Target uplift | 1Y uplift | Confidence | Horizon |",
        "| --- | --- | ---: | ---: | ---: | ---: | --- | --- |",
    ]
    for row in sorted(rows, key=summary_sort_key):
        suggestion = f"{row.action} / {row.rating}"
        lines.append(
            f"| [{row.ticker}]({row.report_link}) | {suggestion} | "
            f"{format_price(row.current_price)} | {format_price(row.price_target)} | "
            f"{format_percent(row.target_uplift)} | {format_percent(row.annualized_uplift)} | "
            f"{row.confidence} | {short_horizon(row.horizon)} |"
        )
    lines.append("")
    return lines


def build_regeneration_skill() -> list[str]:
    return [
        "## Regeneration Skill",
        "",
        "When ticker folders are upgraded with newer report data, regenerate this homepage instead of editing the table by hand:",
        "",
        "1. Add each completed run under `docs/<TICKER>/<YYYYMMDD>_<MODEL>_<YYYYMMDD>_<HHMMSS>/` with `complete_report.md`, `3_trading/trader.md`, and `5_portfolio/decision.md` present.",
        "2. Run `python scripts/build_reports_site.py` from the repository root.",
        "3. For a fixed-date homepage summary, run `python scripts/build_reports_site.py --summary-analysis-date YYYYMMDD --summary-only`.",
        "4. Review `docs/index.md` and the affected `docs/<TICKER>/index.md` files in `git diff`.",
        "5. If a row shows `n/a`, check that the new report includes `**Rating**`, `**Price Target**`, `**Time Horizon**`, `**Action**`, and a latest close/current price line in the market report.",
        "",
        "The generator always chooses the newest run per ticker by analysis date and run-start timestamp.",
        "",
    ]


def build_home(
    by_ticker: dict[str, list[Run]],
    total_runs: int,
    summary_rows: list[SummaryRow],
    summary_analysis_date: str | None = None,
) -> str:
    tickers = sorted(by_ticker)
    lines = [
        "# TradingAgents Reports",
        "",
        f"_{total_runs} runs across {len(tickers)} tickers._",
        "",
        DISCLAIMER,
        "",
        *build_decision_summary(summary_rows, summary_analysis_date),
        *build_regeneration_skill(),
        "## Tickers",
        "",
    ]
    for ticker in tickers:
        n = len(by_ticker[ticker])
        suffix = "run" if n == 1 else "runs"
        lines.append(f"- [{ticker}]({ticker}/index.md) &middot; {n} {suffix}")
    lines.append("")
    return "\n".join(lines) + "\n"


def replace_decision_summary(home_text: str, summary_lines: list[str]) -> str:
    new_summary = "\n".join(summary_lines).rstrip() + "\n\n"
    pattern = re.compile(
        r"^## .+Decision Summary\n.*?(?=^## Regeneration Skill\n)",
        flags=re.M | re.S,
    )
    new_text, replacements = pattern.subn(new_summary, home_text, count=1)
    if replacements != 1:
        raise ValueError("Could not find decision summary block in docs/index.md")
    return new_text


def build_ticker_hub(ticker: str, runs: list[Run]) -> str:
    runs_sorted = sorted(
        runs,
        key=lambda r: (r.analysis_date, r.run_started),
        reverse=True,
    )
    lines = [
        f"# {ticker}",
        "",
        f"_{len(runs_sorted)} run(s)._",
        "",
        "| Analysis date | Model | Run started | Report |",
        "| --- | --- | --- | --- |",
    ]
    for r in runs_sorted:
        link = f"./{r.folder_name}/complete_report.md"
        lines.append(
            f"| {r.analysis_date} | `{r.model}` | {r.run_started} | "
            f"[Open]({link}) |"
        )
    lines.append("")
    return "\n".join(lines) + "\n"


def strip_legacy_front_matter(run: Run) -> bool:
    """Drop any legacy Jekyll/just-the-docs front matter from a run report."""
    path = DOCS_DIR / run.ticker / run.folder_name / "complete_report.md"
    if not path.is_file():
        return False
    body = path.read_text(encoding="utf-8")
    new_body = FRONT_MATTER_RE.sub("", body, count=1)
    if new_body == body:
        return False
    path.write_text(new_body, encoding="utf-8")
    return True


def parse_args(argv: list[str] | None = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--summary-analysis-date",
        help="Use only this analysis date for the homepage decision summary.",
    )
    parser.add_argument(
        "--summary-only",
        action="store_true",
        help="Rewrite only the decision-summary block in docs/index.md.",
    )
    args = parser.parse_args(argv)
    try:
        args.summary_analysis_date = normalize_analysis_date(args.summary_analysis_date)
    except ValueError as exc:
        parser.error(str(exc))
    return args


def main(argv: list[str] | None = None) -> int:
    args = parse_args(argv)
    if not DOCS_DIR.is_dir():
        print(f"docs/ not found at {DOCS_DIR}", file=sys.stderr)
        return 1

    runs: list[Run] = []
    for ticker_dir in sorted(DOCS_DIR.iterdir()):
        if not ticker_dir.is_dir():
            continue
        if not TICKER_DIR_RE.match(ticker_dir.name):
            continue
        if ticker_dir.name == "assets":
            continue
        for run_dir in sorted(ticker_dir.iterdir()):
            run = parse_run_folder(ticker_dir, run_dir)
            if run is None:
                continue
            runs.append(run)

    if not runs:
        print("No run folders found under docs/.", file=sys.stderr)
        return 1

    by_ticker: dict[str, list[Run]] = defaultdict(list)
    for r in runs:
        by_ticker[r.ticker].append(r)

    summary_runs = latest_runs(by_ticker, args.summary_analysis_date)
    if not summary_runs:
        if args.summary_analysis_date:
            print(
                f"No run folders found for analysis date {args.summary_analysis_date}.",
                file=sys.stderr,
            )
        else:
            print("No runs available for homepage summary.", file=sys.stderr)
        return 1

    summary_rows = [build_summary_row(r) for r in summary_runs]

    if args.summary_only:
        index_path = DOCS_DIR / "index.md"
        try:
            index_path.write_text(
                replace_decision_summary(
                    read_text(index_path),
                    build_decision_summary(
                        summary_rows,
                        args.summary_analysis_date,
                    ),
                ),
                encoding="utf-8",
            )
        except ValueError as exc:
            print(str(exc), file=sys.stderr)
            return 1
        source = args.summary_analysis_date or "latest"
        print(f"Wrote docs/index.md decision summary from {source} runs.")
        return 0

    (DOCS_DIR / "index.md").write_text(
        build_home(
            by_ticker,
            total_runs=len(runs),
            summary_rows=summary_rows,
            summary_analysis_date=args.summary_analysis_date,
        ),
        encoding="utf-8",
    )

    for ticker in sorted(by_ticker):
        hub_path = DOCS_DIR / ticker / "index.md"
        hub_path.parent.mkdir(parents=True, exist_ok=True)
        hub_path.write_text(
            build_ticker_hub(ticker, by_ticker[ticker]),
            encoding="utf-8",
        )

    rewritten = 0
    for r in runs:
        if strip_legacy_front_matter(r):
            rewritten += 1

    print(
        f"Wrote docs/index.md, {len(by_ticker)} ticker hubs, and stripped "
        f"legacy front matter from {rewritten} of {len(runs)} run reports."
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
