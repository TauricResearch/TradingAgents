"""Build an MkDocs Material site under ``docs/`` from completed runs.

Each run lives at ``docs/<TICKER>/<DATE>_<MODEL>_<TS>/complete_report.md``.

Layout produced by this script:

* ``docs/index.md`` -- home page with the latest-decision summary, ticker
  list, and regeneration instructions.
* ``docs/<TICKER>/index.md`` -- per-ticker hub page with a table of runs.

MkDocs auto-discovers files for navigation, so per-file front matter is
not required and is stripped from each ``complete_report.md`` if present
(legacy Jekyll/just-the-docs migration).

Run locally:

    python scripts/build_reports_site.py

Idempotent: rewrites ``docs/index.md``, ``docs/<TICKER>/index.md``, and
strips Jekyll front matter from each ``complete_report.md`` in place.
"""
from __future__ import annotations

import argparse
import math
import re
import sys
from collections import defaultdict
from datetime import datetime
from pathlib import Path
from typing import NamedTuple


REPO_ROOT = Path(__file__).resolve().parent.parent
DOCS_DIR = REPO_ROOT / "docs"

TICKER_DIR_RE = re.compile(r"^[A-Za-z0-9.\-]+$")

# Run folder name is <DATE>_<MODEL>[_<RUN_DATE>_<RUN_TIME>]. The optional
# RUN_DATE/RUN_TIME tail records when the run started (vs. the analysis
# date). MODEL is non-greedy so it doesn't swallow the trailing timestamp.
RUN_DIR_RE = re.compile(
    r"^(?P<date>\d{8})_(?P<model>.+?)"
    r"(?:_(?P<run_date>\d{8})_(?P<run_time>\d{6}))?$"
)

DISCLAIMER = (
    "!!! warning \"Disclaimer\"\n"
    "    Reports are generated by an LLM-based research agent for "
    "experimentation. They are **not** financial advice. Treat the analysis "
    "as a synthetic exercise."
)

FRONT_MATTER_RE = re.compile(r"\A---\n.*?\n---\n+", re.DOTALL)


class Run(NamedTuple):
    ticker: str
    analysis_date: str   # YYYY-MM-DD
    model: str
    run_started: str     # YYYY-MM-DD HH:MM:SS
    folder_name: str     # used in URLs


class SummaryRow(NamedTuple):
    ticker: str
    report_link: str
    rating: str
    action: str
    current_price: float | None
    price_target: float | None
    target_uplift: float | None
    annualized_uplift: float | None
    confidence: str
    horizon: str


def parse_run_folder(ticker_dir: Path, run_path: Path) -> Run | None:
    """Parse ``docs/<TICKER>/<RUN>``; ticker comes from the parent dir."""
    if not run_path.is_dir():
        return None
    m = RUN_DIR_RE.match(run_path.name)
    if not m:
        return None
    date = m.group("date")
    analysis_date = f"{date[:4]}-{date[4:6]}-{date[6:8]}"
    rd, rt = m.group("run_date"), m.group("run_time")
    if rd and rt:
        run_started = f"{rd[:4]}-{rd[4:6]}-{rd[6:8]} {rt[:2]}:{rt[2:4]}:{rt[4:6]}"
    else:
        run_started = datetime.fromtimestamp(run_path.stat().st_mtime).strftime(
            "%Y-%m-%d %H:%M:%S"
        )
    return Run(
        ticker=ticker_dir.name.upper(),
        analysis_date=analysis_date,
        model=m.group("model"),
        run_started=run_started,
        folder_name=run_path.name,
    )


def read_text(path: Path) -> str:
    if not path.is_file():
        return ""
    return path.read_text(encoding="utf-8", errors="replace")


def field(text: str, name: str) -> str:
    m = re.search(rf"^\*\*{re.escape(name)}\*\*:\s*(.+)$", text, flags=re.M)
    return m.group(1).strip() if m else ""


def parse_money(value: str) -> float | None:
    value = value.strip().replace(",", "")
    try:
        return float(value)
    except ValueError:
        return None


def normalize_analysis_date(value: str | None) -> str | None:
    if value is None:
        return None
    value = value.strip()
    if re.fullmatch(r"\d{8}", value):
        return f"{value[:4]}-{value[4:6]}-{value[6:8]}"
    if re.fullmatch(r"\d{4}-\d{2}-\d{2}", value):
        return value
    raise ValueError("analysis date must use YYYYMMDD or YYYY-MM-DD")


