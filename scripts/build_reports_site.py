"""Build a just-the-docs Jekyll site for GitHub Pages from docs/ runs.

Each run lives at ``docs/<TICKER>_<DATE>_<MODEL>_<TS>/complete_report.md``.

Layout produced by this script:

* ``docs/index.md`` -- nav root, links to one hub page per ticker.
* ``docs/<TICKER>.md`` -- per-ticker hub page (nav parent), table of runs.
* Each run's ``complete_report.md`` is given just-the-docs front matter
  (``parent: <TICKER>``, ``title: <run-started>``) so it slots into the
  left sidebar under its ticker. Existing front matter is replaced.

Run locally:

    python scripts/build_reports_site.py

Idempotent: rewrites ``docs/index.md``, ``docs/<TICKER>.md``, and the
front matter blocks of each ``complete_report.md`` in place.
"""

from __future__ import annotations

import re
import sys
from collections import defaultdict
from datetime import datetime
from pathlib import Path
from typing import NamedTuple


REPO_ROOT = Path(__file__).resolve().parent.parent
DOCS_DIR = REPO_ROOT / "docs"

RUN_DIR_RE = re.compile(
    r"^(?P<ticker>[A-Za-z0-9.\-]+)_(?P<date>\d{8})_(?P<model>.+?)"
    r"(?:_(?P<run_date>\d{8})_(?P<run_time>\d{6}))?$"
)

DISCLAIMER = (
    "> **Disclaimer:** Reports are generated by an LLM-based research "
    "agent for experimentation. They are **not** financial advice. Treat "
    "the analysis as a synthetic exercise."
)

FRONT_MATTER_RE = re.compile(r"\A---\n.*?\n---\n", re.DOTALL)


class Run(NamedTuple):
    ticker: str
    analysis_date: str   # YYYY-MM-DD
    model: str
    run_started: str     # YYYY-MM-DD HH:MM:SS
    folder_name: str     # used in URLs


def parse_run_folder(path: Path) -> Run | None:
    if not path.is_dir():
        return None
    m = RUN_DIR_RE.match(path.name)
    if not m:
        return None
    date = m.group("date")
    analysis_date = f"{date[:4]}-{date[4:6]}-{date[6:8]}"
    rd, rt = m.group("run_date"), m.group("run_time")
    if rd and rt:
        run_started = f"{rd[:4]}-{rd[4:6]}-{rd[6:8]} {rt[:2]}:{rt[2:4]}:{rt[4:6]}"
    else:
        run_started = datetime.fromtimestamp(path.stat().st_mtime).strftime(
            "%Y-%m-%d %H:%M:%S"
        )
    return Run(
        ticker=m.group("ticker").upper(),
        analysis_date=analysis_date,
        model=m.group("model"),
        run_started=run_started,
        folder_name=path.name,
    )


def build_home(by_ticker: dict[str, list[Run]], total_runs: int) -> str:
    tickers = sorted(by_ticker)
    lines = [
        "---",
        "title: Home",
        "layout: default",
        "nav_order: 1",
        "---",
        "",
        "# TradingAgents Reports",
        "",
        f"_{total_runs} runs across {len(tickers)} tickers._",
        "",
        DISCLAIMER,
        "",
        "## Tickers",
        "",
    ]
    for ticker in tickers:
        n = len(by_ticker[ticker])
        suffix = "run" if n == 1 else "runs"
        lines.append(f"- [{ticker}](./{ticker.lower()}.html) &middot; {n} {suffix}")
    lines.append("")
    return "\n".join(lines) + "\n"


def build_ticker_hub(ticker: str, runs: list[Run], nav_order: int) -> str:
    runs_sorted = sorted(
        runs,
        key=lambda r: (r.analysis_date, r.run_started),
        reverse=True,
    )
    lines = [
        "---",
        f"title: {ticker}",
        "layout: default",
        f"nav_order: {nav_order}",
        "has_children: true",
        "---",
        "",
        f"# {ticker}",
        "",
        f"_{len(runs_sorted)} run(s)._",
        "",
        "| Analysis date | Model | Run started | Report |",
        "| --- | --- | --- | --- |",
    ]
    for r in runs_sorted:
        link = f"./{r.folder_name}/complete_report.html"
        lines.append(
            f"| {r.analysis_date} | `{r.model}` | {r.run_started} | "
            f"[Open]({link}) |"
        )
    lines.append("")
    return "\n".join(lines) + "\n"


def report_front_matter(run: Run) -> str:
    return (
        "---\n"
        f'title: "{run.run_started} &middot; {run.model}"\n'
        f"parent: {run.ticker}\n"
        "layout: default\n"
        "---\n"
    )


def write_report_front_matter(run: Run) -> bool:
    """Add or replace the just-the-docs front matter on a run's report.

    Returns True if the file was rewritten.
    """
    path = DOCS_DIR / run.folder_name / "complete_report.md"
    if not path.is_file():
        return False
    body = path.read_text(encoding="utf-8")
    desired = report_front_matter(run)
    existing = FRONT_MATTER_RE.match(body)
    if existing:
        if existing.group(0) == desired:
            return False
        body = body[existing.end():]
    new_body = desired + body
    if new_body == path.read_text(encoding="utf-8"):
        return False
    path.write_text(new_body, encoding="utf-8")
    return True


def main() -> int:
    if not DOCS_DIR.is_dir():
        print(f"docs/ not found at {DOCS_DIR}", file=sys.stderr)
        return 1

    runs: list[Run] = []
    for child in sorted(DOCS_DIR.iterdir()):
        run = parse_run_folder(child)
        if run is None:
            continue
        runs.append(run)

    if not runs:
        print("No run folders found under docs/.", file=sys.stderr)
        return 1

    by_ticker: dict[str, list[Run]] = defaultdict(list)
    for r in runs:
        by_ticker[r.ticker].append(r)

    (DOCS_DIR / "index.md").write_text(
        build_home(by_ticker, total_runs=len(runs)), encoding="utf-8"
    )

    for nav_order, ticker in enumerate(sorted(by_ticker), start=2):
        hub_path = DOCS_DIR / f"{ticker.lower()}.md"
        hub_path.write_text(
            build_ticker_hub(ticker, by_ticker[ticker], nav_order),
            encoding="utf-8",
        )

    rewritten = 0
    for r in runs:
        if write_report_front_matter(r):
            rewritten += 1

    print(
        f"Wrote docs/index.md, {len(by_ticker)} ticker hubs, and updated "
        f"front matter on {rewritten} of {len(runs)} run reports."
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())