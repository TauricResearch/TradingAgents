    r"Latest close[^$\n]*\$([0-9][0-9,]*(?:\.\d+)?)",
    r"Latest trading day[^\n$]*(?:Last close|close)\s*:?\s*\$([0-9][0-9,]*(?:\.\d+)?)",
    r"last trading day[^\n$]*close\s+\$([0-9][0-9,]*(?:\.\d+)?)",
    r"\*\*Last Close:\*\*\s*\$([0-9][0-9,]*(?:\.\d+)?)",
