def extract_current_price(report_text: str, decision_text: str, trader_text: str) -> float | None:
    # Current means the report-time latest close, not a live quote.
    decision_value = field(decision_text, "Current Price")
    if decision_value:
        return parse_money(decision_value)

    market_text = report_text.split("### Sentiment Analyst", 1)[0]
    for pattern in CURRENT_PRICE_PATTERNS:
        m = re.search(pattern, market_text[:35000], flags=re.I)
        if m:
            return parse_money(m.group(1))
    decision_and_trader = f"{decision_text}\n{trader_text}"
    for pattern in CURRENT_PRICE_FALLBACK_PATTERNS:
        m = re.search(pattern, decision_and_trader, flags=re.I)
        if m:
            return parse_money(m.group(1))
    return parse_money(field(trader_text, "Entry Price"))


def extract_confidence(report_text: str) -> str:
    for pattern in (
        r"^\*\*Confidence:\*\*\s*([A-Za-z]+)",
        r"Confidence is \*\*([A-Za-z]+)\*\*",
        r"Confidence:\s*([A-Za-z]+)",
    ):
        m = re.search(pattern, report_text, flags=re.I | re.M)
        if m:
            return m.group(1).capitalize()
    return "n/a"


def horizon_months(horizon: str) -> float | None:
    h = horizon.lower()
    if "4 quarters" in h:
        return 12.0
    if re.search(r"\bthrough\s+q[1-4]\s+\d{4}\s+earnings\b", h):
        return 3.0
    m = re.search(r"(\d+)\s*\+\s*(?:m\b|months?\b)", h)
    if m:
        return float(m.group(1))
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*days?", h)
    if m:
        return ((float(m.group(1)) + float(m.group(2))) / 2.0) / 30.4375
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*d\b", h)
    if m:
        return ((float(m.group(1)) + float(m.group(2))) / 2.0) / 30.4375
    m = re.search(r"(\d+)\s*d\b", h)
    if m:
        return float(m.group(1)) / 30.4375
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*weeks?", h)
    if m:
        return ((float(m.group(1)) + float(m.group(2))) / 2.0) * 12.0 / 52.0
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*w\b", h)
    if m:
        return ((float(m.group(1)) + float(m.group(2))) / 2.0) * 12.0 / 52.0
    m = re.search(r"(\d+)\s*w\b", h)
    if m:
        return float(m.group(1)) * 12.0 / 52.0
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*quarters?", h)
    if m:
        return ((float(m.group(1)) + float(m.group(2))) / 2.0) * 3.0
    m = re.search(r"(\d+)\s*quarters?", h)
    if m:
        return float(m.group(1)) * 3.0
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*months?", h)
    if m:
        return (float(m.group(1)) + float(m.group(2))) / 2.0
    m = re.search(r"(\d+)\s*months?", h)
    if m:
        return float(m.group(1))
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*m\b", h)
    if m:
        return (float(m.group(1)) + float(m.group(2))) / 2.0
    m = re.search(r"(\d+)\s*m\b", h)
    if m:
        return float(m.group(1))
    m = re.search(r"(\d+)\s*-\s*(\d+)\s*years?", h)
    if m:
        return ((float(m.group(1)) + float(m.group(2))) / 2.0) * 12.0
    m = re.search(r"(\d+)\s*years?", h)
    if m:
        return float(m.group(1)) * 12.0
    return None


def build_summary_row(run: Run) -> SummaryRow:
    run_dir = DOCS_DIR / run.ticker / run.folder_name
    decision_text = read_text(run_dir / "5_portfolio" / "decision.md")
    trader_text = read_text(run_dir / "3_trading" / "trader.md")
    report_text = read_text(run_dir / "complete_report.md")

    current = extract_current_price(report_text, decision_text, trader_text)
    target = extract_price_target(decision_text)
    if target is None and current is not None:
        target = current
    horizon = extract_time_horizon(decision_text)
    target_uplift = None
    annualized_uplift = None
    if current is not None and target is not None and current != 0:
        target_uplift = target / current - 1.0
        months = horizon_months(horizon)
        if months and months > 0 and 1.0 + target_uplift > 0:
            annualized_uplift = math.pow(1.0 + target_uplift, 12.0 / months) - 1.0

    return SummaryRow(
        ticker=run.ticker,
        model=run.model,
        report_link=f"./{run.ticker}/{run.folder_name}/complete_report.md",
        rating=field(decision_text, "Rating") or "n/a",
        action=field(trader_text, "Action") or "n/a",
        current_price=current,
        price_target=target,
        target_uplift=target_uplift,
        annualized_uplift=annualized_uplift,
        confidence=extract_confidence(report_text),
        horizon=horizon or "n/a",
    )


def latest_runs(
    by_ticker: dict[str, list[Run]], analysis_date: str | None = None
) -> list[Run]:
    """Return the newest run per ticker/model pair.

    The homepage should include parallel model evaluations for the same
    ticker and analysis date, while still collapsing reruns of the same
    ticker/model to the newest run-start timestamp.
    """
    analysis_date = normalize_analysis_date(analysis_date)
    latest_by_ticker_model: dict[tuple[str, str], Run] = {}
    for ticker_runs in by_ticker.values():
        if analysis_date:
            ticker_runs = [r for r in ticker_runs if r.analysis_date == analysis_date]
        for run in ticker_runs:
            key = (run.ticker, run.model)
            existing = latest_by_ticker_model.get(key)
            if existing is None or (run.analysis_date, run.run_started) > (
                existing.analysis_date,
                existing.run_started,
            ):
                latest_by_ticker_model[key] = run
    return sorted(
        latest_by_ticker_model.values(),
        key=lambda r: (r.ticker, r.model),
    )


def analysis_dates(by_ticker: dict[str, list[Run]]) -> list[str]:
    dates = {run.analysis_date for ticker_runs in by_ticker.values() for run in ticker_runs}
    return sorted(dates, reverse=True)


def build_daily_summaries(by_ticker: dict[str, list[Run]]) -> list[DailySummary]:
    summaries = []
    for analysis_date in analysis_dates(by_ticker):
        runs = latest_runs(by_ticker, analysis_date)
        if runs:
            summaries.append(
                DailySummary(
                    analysis_date=analysis_date,
                    rows=[build_summary_row(run) for run in runs],
                )
            )
    return summaries


def summary_sort_key(row: SummaryRow) -> tuple[str, str]:
    return (row.ticker, row.model)


def format_price(value: float | None) -> str:
    if value is None:
        return "n/a"
    if value >= 1000:
        return f"${value:,.0f}"
    if value >= 100:
        return f"${value:,.2f}"
    return f"${value:.2f}"


def format_percent(value: float | None) -> str:
    if value is None:
        return "n/a"
    return f"{value * 100:+.1f}%"


def short_horizon(horizon: str) -> str:
    return (
        horizon.replace(" months", "m")
        .replace(" month", "m")
        .replace(" weeks", "w")
        .replace(" week", "w")
        .replace(" days", "d")
        .replace(" day", "d")
    )


def decision_summary_anchor(analysis_date: str) -> str:
    return f"{analysis_date}-decision-summary"


def build_decision_summary(
    rows: list[SummaryRow], analysis_date: str | None = None
) -> list[str]:
    analysis_date = normalize_analysis_date(analysis_date)
    heading = (
        f"## {analysis_date} Decision Summary"
        if analysis_date
        else "## Latest Decision Summary"
    )
    note = (
        f"_Uses the latest {analysis_date} run folder for each ticker/model pair. Current price is the report-time latest close parsed from the report, not a live quote. Target uplift is target/current. 1Y uplift is annualized from the midpoint of the stated horizon, so short-horizon rows can look extreme._"
        if analysis_date
        else "_Uses the latest run folder for each ticker/model pair. Current price is the report-time latest close parsed from the report, not a live quote. Target uplift is target/current. 1Y uplift is annualized from the midpoint of the stated horizon, so short-horizon rows can look extreme._"
    )
    lines = [
        heading,
        "",
        note,
        "",
        "| Ticker | Model | Suggestion | Current | Target | Target uplift | 1Y uplift | Confidence | Horizon |",
        "| --- | --- | --- | ---: | ---: | ---: | ---: | --- | --- |",
    ]
    for row in sorted(rows, key=summary_sort_key):
        suggestion = f"{row.action} / {row.rating}"
        lines.append(
            f"| [{row.ticker}]({row.report_link}) | `{row.model}` | {suggestion} | "
            f"{format_price(row.current_price)} | {format_price(row.price_target)} | "
            f"{format_percent(row.target_uplift)} | {format_percent(row.annualized_uplift)} | "
            f"{row.confidence} | {short_horizon(row.horizon)} |"
        )
    lines.append("")
    return lines


def build_daily_decision_summaries(
    summaries: list[DailySummary],
    focus_analysis_date: str | None = None,
) -> list[str]:
    focus_analysis_date = normalize_analysis_date(focus_analysis_date)
    lines = [
        "## Daily Decision Summaries",
        "",
        '<div class="daily-summary-layout" markdown="1">',
        "",
        '<nav class="daily-summary-rail" aria-label="Analysis dates">',
        "<h3>Dates</h3>",
        "<ul>",
    ]
    for summary in summaries:
        report_count = len(summary.rows)
        suffix = "report" if report_count == 1 else "reports"
        classes = ["daily-summary-date"]
        if focus_analysis_date == summary.analysis_date:
            classes.append("daily-summary-date--active")
        lines.extend(
            [
                "<li>",
                (
                    f'<a class="{" ".join(classes)}" '
                    f'href="#{decision_summary_anchor(summary.analysis_date)}">'
                    f"{summary.analysis_date}</a>"
                ),
                f"<span>{report_count} {suffix}</span>",
                "</li>",
            ]
        )
    lines.extend(
        [
            "</ul>",
            "</nav>",
            "",
            '<div class="daily-summary-tables" markdown="1">',
            "",
        ]
    )
    for summary in summaries:
        lines.extend(build_decision_summary(summary.rows, summary.analysis_date))
    lines.extend(
        [
            "</div>",
            "",
            "</div>",
            "",
        ]
    )
    return lines


