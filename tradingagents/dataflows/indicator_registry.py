INDICATORS: dict[str, IndicatorSpec] = {spec.name: spec for spec in _SPECS}


def get_spec(name: str) -> IndicatorSpec:
    """Look up an indicator by canonical name; unknown names raise ValueError."""
    spec = INDICATORS.get(name)
    if spec is None:
        raise ValueError(
            f"Indicator {name} is not supported. Please choose from: {list(INDICATORS.keys())}"
        )
    return spec


def supported_names() -> list[str]:
    return list(INDICATORS.keys())


def snapshot_indicators() -> tuple[str, ...]:
    """Indicator names included in the verified market snapshot."""
    return tuple(name for name, spec in INDICATORS.items() if spec.in_snapshot)


def effective_params(name: str, indicator_params: dict | None = None) -> dict:
    """The spec's default params overlaid with any configured overrides.

    ``indicator_params`` maps canonical name -> param overrides (the config's
    ``indicator_params`` key); when None, it is read from the live config.
    Unknown override keys are ignored so a config typo cannot produce a
    malformed stockstats column.
    """
    spec = get_spec(name)
    if indicator_params is None:
        from .config import get_config

        indicator_params = get_config().get("indicator_params") or {}
    params = dict(spec.default_params)
    overrides = indicator_params.get(name) or {}
    params.update({k: v for k, v in overrides.items() if k in params})
    return params


def resolve_column(name: str, params: dict | None = None) -> str:
    """The stockstats column to compute for this name with these params.

    Default params resolve to the bare legacy column (``rsi``, ``boll_ub``) so
    behavior is byte-identical when nothing is configured; overridden params
    resolve through ``column_template`` (``rsi_7``, ``boll_ub_30`` — stockstats
    appends the window suffix to the band names only when a window is given).
    """
    spec = get_spec(name)
    if spec.stockstats_column is None:
        raise ValueError(
            f"Indicator {name} is computed from OHLCV, not as a stockstats column."
        )
    if not spec.column_template or params is None or params == spec.default_params:
        return spec.stockstats_column
    return spec.column_template.format(**params)


def render_prompt_section() -> str:
    """The category-grouped indicator menu for the market analyst's prompt."""
    by_category: dict[str, list[IndicatorSpec]] = {}
    for spec in INDICATORS.values():
        if spec.in_prompt:
            by_category.setdefault(spec.category, []).append(spec)

    blocks = []
    for category, specs in by_category.items():
        lines = [f"{category}:"]
        for spec in specs:
            lines.append(f"- {spec.name}: {spec.prompt_description or spec.description}")
        blocks.append("\n".join(lines))
    return "\n\n".join(blocks)
