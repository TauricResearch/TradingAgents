"""Single source of truth for technical-indicator metadata.

Indicator names, descriptions, stockstats column mappings, Alpha Vantage API
mappings and snapshot membership previously lived in three hand-synced copies
(`y_finance.best_ind_params`, `alpha_vantage_indicator.indicator_descriptions`,
and the market analyst's system prompt). Every consumer now reads from this
registry, so adding an indicator is a single ``IndicatorSpec`` entry here:

* ``y_finance.get_stock_stats_indicators_window`` — name validation, stockstats
  column resolution, custom-indicator dispatch, report descriptions.
* ``alpha_vantage_indicator.get_indicator`` — which names the Alpha Vantage API
  can serve and how (function, CSV column, query params).
* ``agents/analysts/market_analyst.py`` — the indicator menu in the system
  prompt is generated by :func:`render_prompt_section`.
* ``market_data_validator`` — the verified-snapshot indicator set comes from
  :func:`snapshot_indicators`.
"""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass(frozen=True)
class IndicatorSpec:
    """Everything the system knows about one technical indicator.

    ``name`` is the canonical, agent-facing identifier and never changes even
    when windows are reconfigured (``rsi`` stays ``rsi`` at window 7).
    """

    name: str
    category: str
    # One-line "Name: ... Usage: ... Tips: ..." text appended to tool output.
    description: str
    # Longer prompt-menu text where it differs from `description` (the tiered
    # multi-timeframe indicators explain their three-tier payload to the agent).
    prompt_description: str | None = None
    # stockstats column computed for this name (None = not a stockstats column).
    stockstats_column: str | None = None
    # Windowed stockstats column form, e.g. "rsi_{window}". None = the window
    # is fixed by stockstats class attributes and cannot be overridden.
    column_template: str | None = None
    default_params: dict = field(default_factory=dict)
    # Dispatch key for indicators computed from OHLCV in stockstats_utils
    # rather than through a stockstats column ("td_9", "z_score", ...).
    custom: str | None = None
    # Alpha Vantage API mapping. av_function None = AV cannot serve this name
    # (the vendor raises so the router falls back to yfinance).
    av_function: str | None = None
    av_column: str | None = None
    av_series_type: bool = True
    av_takes_window: bool = False
    av_fixed_params: dict = field(default_factory=dict)
    # Multiplier applied to stockstats output so all vendors agree on scale
    # (stockstats mfi is 0-1; Alpha Vantage's and the conventional scale is
    # 0-100).
    scale: float = 1.0
    in_snapshot: bool = False
    # Tool-supported names can be kept out of the analyst's prompt menu.
    in_prompt: bool = True


